package programs;
public class inheritance
{
	public static void main(String args[])
	{
		int n=5;
		for(int i=1;i<=n;i++)
		{
			for(int m=n;m>=i;m--)
			{
				System.out.print(" ");
			}
			for(int k=1;k<=i;k++)
			{
				System.out.print(n-i+1);
			}
			System.out.println(" ");
		
					
			}
		}
	}
	

